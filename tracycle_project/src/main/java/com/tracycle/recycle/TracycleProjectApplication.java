package com.tracycle.recycle;

import java.util.Arrays;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

import com.tracycle.recycle.util.JwtInterceptor;

@SpringBootApplication
public class TracycleProjectApplication implements WebMvcConfigurer{

	public static void main(String[] args) {
		SpringApplication.run(TracycleProjectApplication.class, args);
	}
	
	@Autowired
	private JwtInterceptor jwtInterceptor;
	
	@Override
	public void addInterceptors(InterceptorRegistry registry) {
		registry.addInterceptor(jwtInterceptor).addPathPatterns("/**")
		.excludePathPatterns(
			"/comment/getAllComment/**",
			"/comment/getCommentTotalCount/**",
			"/comment/getCommentLimitOffset/**",
			"/board/**",
			"/user/**"
		)
		.addPathPatterns("/board/writeBoard")
		.addPathPatterns("/board/updateBoard")
		.addPathPatterns("/board/deleteBoard/**");
		
	}

	@Override
	public void addCorsMappings(CorsRegistry registry) {
		registry.addMapping("/**")
		.allowedOrigins("*")
		.allowedMethods("*")
		.allowedHeaders("*")
		.exposedHeaders("jwt-auth-token");
	}
}
